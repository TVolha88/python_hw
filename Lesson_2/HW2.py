# Творческое задание: Определите массовые доли двух выданных веществ (%), округлив из до большего числа. Массы веществ
# необходимо ввести

import math

sub_1 = int(input("Enter the weight of 1 substance:"))  # масса первого вещества
sub_2 = int(input("Enter the weight of 2 substance:"))  # масса второго вещества

w = sub_1 / (sub_1 + sub_2) * 100  # расчет массовой доли вещества(w) = масса 1 вещества /массу всего раствора * 100 %

math.ceil(w)  # округление до большего числа
print(w)  # выведение результата в процентах

print("===========================================================")

