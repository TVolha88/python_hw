# Задача №3 (задача на логику)
# Улитка в день проползает 2м, но за ночь сползает на 1 м. За сколько дней
# улитка доползает, если дерево в высоту 20м

tree = 20  # высота дерева
snail_lift = 2  # подьем улитки за сутки
snail_crawl = 1  # сползание за сутки

snail_liftlastday = tree - snail_lift  # высота улитки накануне достижения вершины
snail_speed = snail_lift - snail_crawl  # перемещение улитки за сутки
days_amount = snail_liftlastday / snail_speed + 1  # разделить высоту накануне достижения вершины на суточное перемещение прибавить к нему 1 день (последний подъем)

print(days_amount)
